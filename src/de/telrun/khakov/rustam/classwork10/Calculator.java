public class Calculator {


	public double mult(double first, double sec) {
		return first * sec;
	}

	public double div(double first, double sec) {
		return first / sec;
	}

	public double sum(double first, double sec) {
		return first + sec;
	}

	public double minus(double first, double sec) {
		return first - sec;
	}
}
